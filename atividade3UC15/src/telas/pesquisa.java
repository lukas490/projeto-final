    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
    filmesDao dao = new filmesDao();
    dao.conectar();
    String nome = txtPesquisa.getText();
        
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setRowCount(0);
        
        List<filmes> resultados = dao.consultar(nome);
        for (filmes cad : resultados) {
            modelo.addRow(new Object[]{cad.getIdTabela(), cad.getNome(), cad.getGenero(), cad.getProdutor(), cad.getAno()});
        }
        dao.desconectar();
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
        if (tabela.getSelectedRow() >= 0) {
            String id = (String) tabela.getValueAt(tabela.getSelectedRow(), 0);
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir o registro " + id + "?");

            if (resposta == JOptionPane.YES_OPTION) {   
                filmesDao f = new filmesDao();    
                boolean conectado = f.conectar();

                if (!conectado) {
                    JOptionPane.showMessageDialog(this, "Erro de conexão com o banco");
                    return;
                }

                boolean sucesso = f.excluir(Integer.parseInt(id));

                if (sucesso) {
                    JOptionPane.showMessageDialog(this, "Registro excluído com sucesso");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir o registro");
                }

                f.desconectar(); 
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

