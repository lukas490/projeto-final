    private void configurarPermissoes() {
        switch (tipoUsuario) {
            case "Administrador":
                btnTelaCadastrar.setEnabled(true);
                btnTelaAtualizar.setVisible(true);
                btnExcluir.setEnabled(true);
                break;
            case "Operador":
                btnTelaCadastrar.setEnabled(true);
                btnTelaAtualizar.setVisible(true);
                btnExcluir.setEnabled(false);
                break;
            case "Usuário":
                btnTelaCadastrar.setEnabled(false);
                btnTelaAtualizar.setVisible(false);
                btnExcluir.setEnabled(false);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Tipo de usuário desconhecido!");
        }}

